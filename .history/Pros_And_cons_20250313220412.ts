/**
 * =======================================Static typing =================================>
 * 1. Why? Static typing allows you to catch errors at compile time rather than runtime. This means that you can catch errors before they happen, which can save you time and money.
 * 2. How? Typescript uses static typing to enforce type safety. This means that you can specify the type of a variable when you declare it, and the compiler will check that the variable is only assigned values of that type.
 * example = let message: string = "Hello World";
 * console.log(message);
 * 3. Pros:
 * - Catch errors at compile time
 * - Improve code quality
 * - Make code easier to read and understand
 * 4. Cons:
 * - More verbose
 * - Slower development time
 * - More complex code performance 
 */